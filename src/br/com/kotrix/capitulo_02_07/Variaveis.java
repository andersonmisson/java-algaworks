package br.com.kotrix.capitulo_02_07;

public class Variaveis {
	
	public static void main(String[] args) {
		
		int quantidade; // Declarando variavel inteiro
		quantidade = 10; // atribuindo o valor 10
		
		System.out.println(quantidade);
		
		quantidade = 15;
		
		System.out.println(quantidade);
	}

}
