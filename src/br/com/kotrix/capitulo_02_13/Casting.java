package br.com.kotrix.capitulo_02_13;

public class Casting {

	public static void main(String[] args) {
		
		long x = 9300000035L;
		
		// casting
		int y = (int)x;
		System.out.println(y);
		
		int z = 10234;
		
		long z1 = z;
		System.out.println(z1);
		
		double a = 20.5;
		
		float b = (float)a;
		System.out.println(a);

	}

}
